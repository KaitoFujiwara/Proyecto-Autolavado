    private void guardarCliente() {
        try {
            int id = Integer.parseInt(jTextField1.getText().trim());
            String nombre = jTextField2.getText().trim();
            String telefono = jTextField3.getText().trim();

            if (nombre.isEmpty() || telefono.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Llena nombre y teléfono");
                return;
            }

            Cliente cliente = new Cliente(id, nombre, telefono);
            controlador.agregarCliente(cliente);
            JOptionPane.showMessageDialog(this, "Cliente guardado");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID del cliente debe ser numérico");
        }
    }

    private void guardarAuto() {
        try {
            int id = Integer.parseInt(jTextField4.getText().trim());
            String modelo = jTextField6.getText().trim();
            String color = jTextField7.getText().trim();

            if (modelo.isEmpty() || color.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Llena modelo y color");
                return;
            }

            Auto auto = new Auto(id, color, modelo);
            controlador.agregarAuto(auto);

            JOptionPane.showMessageDialog(this, "Auto guardado");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El ID del auto debe ser numérico");
        }
    }

    private void guardarServicio() {
        try {
            int id = Integer.parseInt(jTextField8.getText().trim());
            String nombre = jTextField9.getText().trim();
            String descripcion = jTextField10.getText().trim();
            double precio = Double.parseDouble(jTextField11.getText().trim());
            int duracion = Integer.parseInt(jTextField12.getText().trim());

            if (nombre.isEmpty() || descripcion.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Llena nombre y descripción");
                return;
            }

            Servicio servicio = new Servicio(id, nombre, descripcion, precio, duracion);
            controlador.agregarServicio(servicio);

            JOptionPane.showMessageDialog(this, "Servicio guardado");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID, precio y duración deben ser numéricos");
        }
    }
    
